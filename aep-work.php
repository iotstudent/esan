                <p>
                    The EEEW was hosted in turns, on a rational basis by the five (5) Local Government Areas (LGA) that make up Esanland,
                    namely; Esan West LGA, Ekpoma; Esan North East LGA, Uromi; Esan Central LGA, Irrua; Esan South East, Ubiaja; and Igueben
                    LGA, Igueben respectively. From the above, it was conceptually expedient that the next five (5) EEEWs, spanning over
                    five years were devoted to agriculture, agribusiness and vocational investments. Worthy of note are the EEEW in Irrua,
                    Ubiaja and Ekpoma.
                </p>
                <p>
                    On the heels of the aforementioned potentials in Esanland, the 3rd EEEW in Irrua on September 30, 2006 sought to, among
                    others, engage the local people, trade groups, the private sector and communities through dialogue to start the formal
                    training to extension services; while also focusing on source of funding cooperative groups for development. Titled
                    “Implementing the Vision of Building Groups for Economic Empowerment using Cooperatives as Tools, the workshop attracted
                    over one thousand (1,000) participants and about forty-two (42) cooperative societies including herdsmen from Northern
                    Nigeria who live and engaged in cattle trading across major cities like Uromi and Ekpoma.
                </p>

