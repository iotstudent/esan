                        <p>
                            For this purpose, AEP set up five (5) committees, namely; Poultry Committee chaired by Mr. Emmanuel Ijewere; Cassava
                            Committee chaired by Barr. Matthew Egbadon; Welder/Artisan Trade Association Committee chaired by Chief (Dr.) Leemon
                            Ikpea; Pineapple Committee chaired by Mr. Godday Abumere; and Micro Finance Committee chaired by Chief (Dr.) Pascal
                            Ebhohimen. After the initial keynote paper on “The role of Cooperative in Economic Development of Nigeria, presented by
                            Mr. S. A. Inegbenosun, Deputy Director, Cooperatives, Edo State, the workshop went into break-out sessions of the
                            various committee with a view to drill down on the prospects, challenges and possible assistance from AEP. It must be
                            emphasized that the successes of the 3rd EEEW in Irrua gave birth to various initiatives. However, it is important to
                            note, that the Welder/Artisan Trade Association Committee which was chaired by Chief (Dr.) Leemon Ikpea laid the
                            foundation for the AEP sponsored PTI training that we celebrate today.
                        </p>
                        <p>
                            Taking a cue from the successes of the Irrua workshop, the 4th EEEW in Ubiaja on September 28, 2007 push the boundary
                            from ideas and thought process to actual execution and implementation in a visible look and feel. Themed ‘Nucleus Estate
                            Initiative: Empowering Coopertives in Esanland, the Chairman of the Poultry Committee at the Irrua workshop, Mr.
                            Emmanuel Ijewere in conjunction with the Poultry Farmers Association led by Engr. Omoria Iyomon collaborated with the
                            Association of Esan Professionals (AEP) to promote Virgin Foods Nigeria Limited, a Nucleus Estate Initiative for the
                            processing, packaging and marketing of poultry products. The ultra-modern processing plant which was commissioned by the
                            then Governor of Edo State had the capacity to process birds and other poultry products to assured quality standards for
                            the entire South-South and South-East regions.
                        </p>
                        <p>
                            In continuation of the capacity building in agricbusiness and vocational opportunities, AEP again collaborated with
                            Uhiele Farms Training Centre (UFTC) promoted by Engr. Charles Osezua. The process commenced with a train-the-trainer
                            programme, who was then enrolled in Shongai Farms Centre, Port Novo, Benin Republic to acquire requisite skills and
                            henceforth, train participants. The UFTC was designed as a community-based crop and animal production enterprise
                            development programme aimed at providing practical and interactive leadership training and management courses in crop
                            production and animal husbandry – poultry/piggery while offering starter packs or grants to best performing
                            participants. In 2008, over 28 participants drawn from across Esanland and beyond were accommodated in Uhiele Farms for
                            a period of ten (10) months or weeks to be trained on improved farming techniques and agric-business process. This
                            naturally dovetailed into the 6th EEEW in Ekpoma titled “Positioning Esanland for Emerging Opportunities in Agro-allied
                            Industries”.
                        </p>
                        <p>
                            For a period of six years, spanning the 7th to the 12th EEEW, AEP focused on education and Esan cultural
                            values/language. In the area of education, worthy of note is the enormous data gathering on the decay of educational
                            infrastructure by Mr. Mike Osime, then President of AEP, in the five (5) LGAs in Esanland which prompted Edo State
                            Government’s intervention that led to the now famous “Red Roof” across the entire Edo State.
                        </p>
                        <p>
                            At the 2017 EEEW, capacity building in agricbusiness and vocational potentials in Esanland regained centre stage at the
                            Uromi workshop themed “Modernizing Agricbusiness in Esanland for Economic Empowerment” Boosted by the current drive by
                            the Federal Government on Agribusiness as well as our vision to empower Esan people, AEP, like 2006, reached out to
                            cooperative societies and trade associations. In total, 37 cooperatives and 166 guests attended the Day One event which
                            had farmers’ training session.
                        </p>
                        <p>
                            In July 2017, AEP, led by Barr. Matthew Egbadon, the immediate past president signed a memorandum of understanding (MOU)
                            with the management of PTI on skill acquisition for Esan youths. Pursuant to that MoU, the Association has so far sent
                            two batches of young graduates of Esan extraction to the institute for a nine-month training in the acquisition of
                            special skills in welding and fabrication techniques under the Association’s Skill Acquisition Programme.
